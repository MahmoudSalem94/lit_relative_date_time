/// A milisecond.
///
/// The smallest unit available for time relative date comparisons.
const double MS = 1;

/// The miliseconds per second `1000`.
const double MS_PER_SECOND = 1000.0;

/// The milliseconds per minute `60000`.
const double MS_PER_MINUTE = 60000.0;

/// The milliseconds per hour `3.6e+6`.
const double MS_PER_HOUR = 3.6e+6;

/// The miliseconds per day `8.64e+7`.
const double MS_PER_DAY = 8.64e+7;

/// The miliseconds per week `6.048e+8`.
const double MS_PER_WEEK = 6.048e+8;

/// The miliseconds per month `2.628e+9`.
const double MS_PER_MONTH = 2.628e+9;

/// The miliseconds per year `3.154e+10`.
const double MS_PER_YEAR = 3.154e+10;
